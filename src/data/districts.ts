// Get districts grouped by region
export function getDistrictsByRegion(): Region[] {
  const regionMap = new Map<string, District[]>();

  districts.forEach(district => {
    const existing = regionMap.get(district.region) || [];
    existing.push(district);
    regionMap.set(district.region, existing);
  });

  return Array.from(regionMap.entries()).map(([name, districts]) => ({
    name,
    districts,
  }));
}

// Get district by code
export function getDistrictByCode(code: string): District | undefined {
  return districts.find(d => d.code === code);
}
